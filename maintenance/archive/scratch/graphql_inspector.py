def extract_post_urls(dump: list[dict]) -> list[str]:
    """Sau khi capture, tổng hợp tất cả URL trông như post/reel."""
    urls = set()
    def _find_urls(obj):
        if isinstance(obj, str):
            if "facebook.com/reel/" in obj or "facebook.com/posts/" in obj or "/share/v/" in obj:
                urls.add(obj)
        elif isinstance(obj, dict):
            for v in obj.values():
                _find_urls(v)
        elif isinstance(obj, list):
            for item in obj:
                _find_urls(item)
    for entry in dump:
        _find_urls(entry.get("response_body"))
    return sorted(urls)


