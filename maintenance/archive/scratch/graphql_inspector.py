def on_response(response: Response):
    """Handler: bắt mọi response từ FB GraphQL."""
    if GRAPHQL_URL_KEY not in response.url:
        return

    try:
        # Bỏ qua nếu không phải JSON
        ct = response.headers.get("content-type", "")
        if "json" not in ct and "javascript" not in ct:
            return

        body = response.json()
    except Exception:
        # Thử parse text nếu json() fail (FB đôi khi trả multi-line JSON)
        try:
            raw = response.text()
            # FB đôi khi trả nhiều JSON objects trên từng dòng
            lines = [l.strip() for l in raw.split("\n") if l.strip()]
            body = []
            for line in lines:
                try:
                    body.append(json.loads(line))
                except Exception:
                    pass
            if not body:
                return
        except Exception:
            return

    # Lấy request info
    req = response.request
    try:
        post_data_raw = req.post_data or ""
        # Parse form-encoded
        req_params = {}
        for part in post_data_raw.split("&"):
            if "=" in part:
                k, _, v = part.partition("=")
                from urllib.parse import unquote_plus
                req_params[unquote_plus(k)] = unquote_plus(v)
    except Exception:
        req_params = {}

    doc_id    = req_params.get("doc_id", "?")
    fb_api_req_friendly_name = req_params.get("fb_api_req_friendly_name", "unknown")
    variables_raw = req_params.get("variables", "{}")

    try:
        variables = json.loads(variables_raw)
    except Exception:
        variables = variables_raw

    # Extract thông tin hữu ích từ response
